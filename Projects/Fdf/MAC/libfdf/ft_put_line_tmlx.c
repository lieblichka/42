#include "mlx.h"
#include "libfdf.h"

void	ft_put_line_tmlx(t_mlx *m)
{
	int x;

	x = 500;
	while (((*m).x)++ < x)
		ft_pixel_put_tmlx(m, (*m).x, (*m).y, 1000);
}
