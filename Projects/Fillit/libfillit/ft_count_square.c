#include "libfillit.h"

int	ft_count_square(char **tetra)
{
	int i;
	int a;
	int b;

	i = -1;
	a = 0;
	while (++i < 4)
		if (ft_strchr(*(tetra + i), '#'))
			a += 1;
	i = -1;
	b = 0;
	while (++i < 4)
		if (tetra[0][i] == '#' ||
				tetra[1][i] == '#' ||
					tetra[2][i] == '#' ||
						tetra[3][i] == '#')
			b += 1;
	return (a > b ? a : b);
}
