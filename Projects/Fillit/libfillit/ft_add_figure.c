void	ft_add_figure(char **tetra)
{
	int i;

	i = -1;
	while (*(*tetra + ++i) != '\0')
		if (*(*tetra + i) != '.')
			*(*(tetra + 16) + i) = *(*tetra + i);
	if (*(*(tetra + 1) + 0) != '\0')
		ft_add_figure(tetra + 1);
}
