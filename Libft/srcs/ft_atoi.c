int		ft_atoi(const char *str)
{
	int	nb;
	int sign;
	int i;

	i = 0;
	nb = 0;
	sign = 1;
	while ((*str >= '\a' && *str <= '\r') || *str == '0' || *str == ' ')
		str++;
	while (*str == '+' && !(*(str + 1) == '-' || *(str + 1) == '+'))
		str++;
	while (*str == '-' && !(*(str + 1) == '-' || *(str + 1) == '+'))
	{
		sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str++ - '0';
		i++;
	}
	if (i > 19)
		return (-1);
	return (nb * sign);
}
