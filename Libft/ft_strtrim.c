#include "libft.h"

char	*ft_strtrim(char const *s)
{
	char	*str;
	int		i;

	i = 0;
	str = (char*)s;
	while (*(str + i) == ' ' || *(str + i) == '\n' || *(str + i) == '\t')
		i++;
	while (*(str + i))
		i++;
	return (str + i);
}
